package com.xiaojiang.sbu.justwifi;

public class ImageUnit {

    private String name;
    private int imageId;
    public ImageUnit(String name, int imageId){
        this.name = name;
        this.imageId = imageId;
    }
    public String getName() {
        return name;
    }
    public int getImageId() {
        return imageId;
    }


}
